package com.kh.day02.loop;

public class Exam_For {
	public static void main(String[] args) {
		System.out.println("ㅎㅇ");
		
		
		//1.For 문
		for (int i=1; i<=10; i++) {
			System.out.println(i);
		}
		
		//2.While 문
		int n =1;
		
		while(n<=10) {
			System.out.println(n);
			n++;
		}
	
		//3.do While 문
		int e = 0;
		do {
			System.out.println(e);
			e++;
		}while(e<1) ;
			
		
	}
	
}
